/**
 * Generates public/firebase-messaging-sw.js from NEXT_PUBLIC_FIREBASE_* env vars.
 * Run automatically via "prebuild" script in package.json.
 *
 * If env vars are not set (CI without Firebase), writes a no-op stub so the build succeeds.
 */
import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const config = {
  apiKey:            process.env.NEXT_PUBLIC_FIREBASE_API_KEY            ?? '',
  authDomain:        process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN        ?? '',
  projectId:         process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID         ?? '',
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID ?? '',
  appId:             process.env.NEXT_PUBLIC_FIREBASE_APP_ID              ?? '',
};

const hasConfig = Object.values(config).some(Boolean);

const content = hasConfig
  ? `// Auto-generated by scripts/gen-firebase-sw.mjs — do not edit manually.
importScripts('https://www.gstatic.com/firebasejs/10.13.2/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.13.2/firebase-messaging-compat.js');

firebase.initializeApp(${JSON.stringify(config, null, 2)});

const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const n = payload.notification ?? {};
  const d = payload.data ?? {};
  const title = n.title ?? d.title ?? 'Sychar';
  const options = {
    body:    n.body ?? d.body,
    icon:    n.icon ?? d.icon ?? '/icon-192.png',
    badge:   '/icon-192.png',
    tag:     n.tag ?? d.tag,
    data:    payload.data,
    vibrate: [200, 100, 200],
  };
  self.registration.showNotification(title, options);
});
`
  : `// Firebase not configured — stub service worker.
// Set NEXT_PUBLIC_FIREBASE_* env vars and re-run build to enable FCM.
self.addEventListener('install', () => self.skipWaiting());
self.addEventListener('activate', () => self.clients.claim());
`;

writeFileSync(resolve(__dirname, '../public/firebase-messaging-sw.js'), content, 'utf8');
console.log('[gen-firebase-sw]', hasConfig ? 'FCM service worker written.' : 'Firebase not configured — stub written.');
